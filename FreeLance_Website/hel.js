
	
	function hasTickedBox() {

	    var chkbx = document.getElementById("termsChkbx");
	    if ( chkbx.checked ) {
		       	 document.getElementById('termsP').style.color = "#efefb5";
		         document.getElementById('termsP').style.backgroundColor= "#008329";
		         document.getElementById('termsP').style.borderRadius = '4px';
 				 document.getElementById('termsP') .style.MozBorderRadius = '4px';
 				 document.getElementById('termsP') .style.padding = '2px';
		    }
		    else{
		    	 document.getElementById('termsP').style.color = "grey";
		    	 document.getElementById('termsP').style.backgroundColor= "white";
		    }
	}

